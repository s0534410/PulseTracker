    private final BroadcastReceiver mGattUpdateReceiver = new BroadcastReceiver() {
        @Override
        public void onReceive(Context context, Intent intent) {
            final String action = intent.getAction();
            if (BluetoothLeService.ACTION_GATT_SERVICES_DISCOVERED.equals(action)) {
                // Show all the supported services and characteristics on the user interface.
                Log.d(TAG, "ACTION_GATT_SERVICES_DISCOVERED");

//                displayGattServices(mBluetoothLeService.getSupportedGattServices());
                subscribeToHeartrateMeasurementCharacteristic(context);

            } else if (BluetoothLeService.ACTION_GATT_DISCONNECTED.equals(action)) {
                Log.d(TAG, "ACTION_GATT_DISCONNECTED");
            }
        }

        private void subscribeToHeartrateMeasurementCharacteristic(final Context context) {

            UUID heartrateService = UUID.fromString("0000180d-0000-1000-8000-00805f9b34fb");
            BluetoothGattCharacteristic bluetoothGattCharacteristic = null;

            for (BluetoothGattService service : mBluetoothLeService.getSupportedGattServices()) {
                if (service.getUuid().equals(heartrateService)) {
                    bluetoothGattCharacteristic = service.getCharacteristic(UUID.fromString(SampleGattAttributes.HEART_RATE_MEASUREMENT));

                    if ((bluetoothGattCharacteristic.getProperties() | BluetoothGattCharacteristic.PROPERTY_READ) > 0) {
                        if (mNotifyCharacteristic != null) {
                            mBluetoothLeService.setCharacteristicNotification(
                                    mNotifyCharacteristic, false);
                            mNotifyCharacteristic = null;
                        }
                        mBluetoothLeService.readCharacteristic(bluetoothGattCharacteristic);
                    }
                    if ((bluetoothGattCharacteristic.getProperties() | BluetoothGattCharacteristic.PROPERTY_NOTIFY) > 0) {
                        mNotifyCharacteristic = bluetoothGattCharacteristic;
                        mBluetoothLeService.setCharacteristicNotification(
                                bluetoothGattCharacteristic, true);
                    }

                    Intent intent = new Intent(context, ShowEventActivity.class);
                    intent.putExtra(DeviceControlActivity.EXTRAS_DEVICE_ADDRESS, mDeviceAddress);
                    startActivity(intent);
                }
            }
        }
    };

    private static IntentFilter makeGattUpdateIntentFilter() {
        final IntentFilter intentFilter = new IntentFilter();
        intentFilter.addAction(BluetoothLeService.ACTION_GATT_SERVICES_DISCOVERED);
        return intentFilter;
    }

    // Device scan callback.
    private BluetoothAdapter.LeScanCallback mLeScanCallback =
            new BluetoothAdapter.LeScanCallback() {

                @Override
                public void onLeScan(final BluetoothDevice device, int rssi, byte[] scanRecord) {
                    runOnUiThread(new Runnable() {
                        @Override
                        public void run() {
                            mLeDeviceListAdapter.addDevice(device);
                            mLeDeviceListAdapter.notifyDataSetChanged();
                        }
                    });
                }
            };

    static class ViewHolder {
        TextView deviceName;
        TextView deviceAddress;
    }
